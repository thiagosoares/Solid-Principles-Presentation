package br.com.sofitplan.trainings.solid.lsp.violation.quadrado;

public class Retangulo {

    private int x;
    private int y;

    public Retangulo(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
